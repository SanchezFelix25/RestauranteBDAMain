    private void inicializarTabla() {
        // Cargar la imagen de lupa
        ImageIcon lupaIcon = new ImageIcon(getClass().getResource("/lupa2.png"));

        // Modelo de la tabla
        DefaultTableModel model = new DefaultTableModel(
                new Object[]{"Comanda", "Mesa", "Estado", "Revisar"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Todas las celdas no son editables
            }
        };

        JTable table = new JTable(model);

        try {
            // Cargar la fuente Montserrat
            Font montserrat = Font.createFont(Font.TRUETYPE_FONT, new File("resources/Montserrat-Medium.ttf")).deriveFont(12f);
            Font montserratHeader = montserrat.deriveFont(Font.BOLD, 14f);

            // Aplicar al header
            JTableHeader header = table.getTableHeader();
            header.setFont(montserratHeader);

            // Aplicar a las filas
            table.setFont(montserrat);
            table.setRowHeight(40); // Ajustar altura de las filas

        } catch (FontFormatException | IOException e) {
            e.printStackTrace();
            // Si falla la carga de fuente, se usa la predeterminada
        }

        // Centrar columnas de texto
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        table.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        table.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        table.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);

        // Renderizador para la columna de icono
        table.getColumnModel().getColumn(3).setCellRenderer(new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus,
                    int row, int column) {
                JLabel label = new JLabel();
                label.setHorizontalAlignment(JLabel.CENTER);
                label.setIcon(lupaIcon);
                return label;
            }
        });
        
        table.setRowHeight(40);
        JTableHeader header = table.getTableHeader();
        header.setFont(new Font("Montserrat", Font.BOLD, 14));
        header.setBackground(new Color(60, 63, 65)); // Fondo gris oscuro
        header.setForeground(Color.WHITE); // Texto blanco
        header.setBorder(BorderFactory.createLineBorder(Color.BLACK, 1)); // Borde negro delgado

        // Crear JScrollPane y agregar la tabla
        JScrollPane scrollPane = new JScrollPane(table);
        scrollPane.setBounds(20, 70, 760, 350); // Ubicar la tabla debajo del texto
        fondo.setLayout(null); // Layout absoluto
        fondo.add(scrollPane); // AÃ±adir JScrollPane al panel fondo

    
    }

